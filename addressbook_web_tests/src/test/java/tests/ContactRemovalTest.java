package tests;// Generated by Selenium IDE

import common.CommonFunctions;
import model.ContactData;
import model.GroupData;
import org.junit.jupiter.api.Assertions;
import org.junit.jupiter.api.Test;

import java.util.ArrayList;
import java.util.Random;

public class ContactRemovalTest extends TestBase{

    @Test
    public void CanRemoveContact() {
        if (app.hbm().getCountContact() == 0) {
            app.hbm().createContact(new ContactData("", "Java", "Tester", "Russia", "111222333", "test@mail.ru", "", "", "", "", "", "", ""));
        }
        var oldContacts = app.hbm().getContactList();
        var rnd = new Random();
        var index = rnd.nextInt(oldContacts.size());
        app.contacts().remove(oldContacts.get(index));
        var newContacts = app.hbm().getContactList();
        var expectedList = new ArrayList<>(oldContacts);
        expectedList.remove(index);
        Assertions.assertEquals(newContacts, expectedList);
    }

    @Test
    void CanRemoveAllContactOnce() {
        if (app.contacts().getCountContact() == 0) {
            app.contacts().create(new ContactData("", "firstname", "lastname", "address", "phone", "email", "", "", "", "", "", "", ""));
        }
        app.contacts().removeAllContact();
        Assertions.assertEquals(0, app.contacts().getCountContact());
    }

    @Test
    void canRemoveContactFromGroup() {
        if (app.hbm().getGroupList().isEmpty()) {
            app.groups().createGroup(new GroupData("", "group name", "group header", "group footer"));
        } if (app.hbm().getContactList().isEmpty()) {
            var contact = new ContactData()
                    .withFirstname(CommonFunctions.randomString(10))
                    .withLastname(CommonFunctions.randomString(10));
            app.contacts().create(contact);
        }

        var allGroups = app.hbm().getGroupList();
        ContactData targetContact = null;
        GroupData targetGroup = null;

        for (var g : allGroups) {
            var contactsInGroup = app.hbm().getContactsInGroup(g);
            if (!contactsInGroup.isEmpty()) {
                targetGroup = g;
                targetContact = contactsInGroup.get(0);
                break;
            }
        }

        if (targetGroup == null || targetContact == null) {
            if (app.hbm().getGroupList().isEmpty()) {
                app.groups().createGroup(new GroupData("", "autotest group", "header", "footer"));
            }

            targetGroup = app.hbm().getGroupList().get(app.hbm().getGroupList().size() - 1);
            var contactToAdd = app.hbm().getContactList().get(0);

            app.contacts().returnHome();
            app.contacts().addToGroup(contactToAdd, targetGroup);
            app.contacts().returnHome();
            var contactsInGroup = app.hbm().getContactsInGroup(targetGroup);
            targetContact = contactsInGroup.get(0);
        }

        var oldRelated = app.hbm().getContactsInGroup(targetGroup);
        app.contacts().removeFromGroup(targetContact, targetGroup);
        app.contacts().returnHome();
        var newRelated = app.hbm().getContactsInGroup(targetGroup);
        Assertions.assertEquals(oldRelated.size() - 1, newRelated.size());
        Assertions.assertFalse(newRelated.contains(targetContact));
    }
}

